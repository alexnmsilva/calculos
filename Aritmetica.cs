using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculos
{
    /// <summary>
    /// Implementa operações aritméticas.
    /// </summary>
    internal  class Aritmetica
    {
		public static int Somar(int x, int y)
        {
            return x + y;
        }
		
		public static int Subtrair(int x, int y)
        {
            return x - y;
        }
    }
}
